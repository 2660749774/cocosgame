    self.confId = confId
    self.blockArray = blockArray
    self.nextBlockArray = nextBlockArray
    self.blockType = blockType
    self.maxBlockNum = maxBlockNum
    self.targetName = "方块迷阵"

    TetrisMazeConf.configMap[confId] = self
end

